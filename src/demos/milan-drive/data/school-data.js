export const getCourseById = (id) => courses.find(c => c.id === id)

export const getPopularCourses = () => courses.filter(c => c.popular)

export const getInstructorsByLanguage = (lang) =>
  instructors.filter(i => i.languages.includes(lang))

export const formatLanguages = (langs, currentLang) => {
  const langNames = {
    it: { it: 'Italiano', en: 'Italian', zh: '意大利语' },
    en: { it: 'Inglese', en: 'English', zh: '英语' },
    zh: { it: 'Cinese', en: 'Chinese', zh: '中文' }
  }
  return langs.map(l => langNames[l][currentLang]).join(', ')
}
