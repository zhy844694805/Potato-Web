// Helper functions
export const getPropertyById = (id) => properties.find(p => p.id === id)

export const getPropertiesByType = (type) => {
  if (type === 'all') return properties
  return properties.filter(p => p.type === type)
}

export const getPropertiesByArea = (areaId) => {
  if (areaId === 'all') return properties
  return properties.filter(p => p.area === areaId)
}

export const getFeaturedProperties = () => properties.filter(p => p.featured)

export const getAreaById = (id) => areas.find(a => a.id === id)

export const getPropertyCountByArea = (areaId) => properties.filter(p => p.area === areaId).length

export const formatPrice = (price, type, lang = 'it') => {
  const formatted = new Intl.NumberFormat('it-IT').format(price)
  if (type === 'rent') {
    const suffix = lang === 'zh' ? '/月' : lang === 'en' ? '/mo' : '/mese'
    return `${formatted}${suffix}`
  }
  return formatted
}

export const getTranslation = (lang, path) => {
  const keys = path.split('.')
  let value = translations[lang]
  for (const key of keys) {
    value = value?.[key]
  }
  return value || path
}
