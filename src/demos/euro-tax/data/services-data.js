// Helper functions
export const getServiceById = (id) => {
  return services.find(s => s.id === parseInt(id))
}

export const getTeamMemberById = (id) => {
  return teamMembers.find(m => m.id === parseInt(id))
}

export const getTranslation = (lang, path) => {
  const keys = path.split('.')
  let value = translations[lang]
  for (const key of keys) {
    value = value?.[key]
  }
  return value || path
}
